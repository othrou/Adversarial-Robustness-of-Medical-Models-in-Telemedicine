        axes[1, 1].set_ylabel('Taux de Refus')
        axes[1, 1].grid(True, alpha=0.3)
        axes[1, 1].set_ylim(0, 1)

        plt.tight_layout()
        plt.savefig('rpo_evaluation_report.png', dpi=300, bbox_inches='tight')
        plt.show()

